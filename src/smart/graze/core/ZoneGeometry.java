package smart.graze.core;

import java.util.ArrayList;
import java.util.List;



public class ZoneGeometry {
	private String type;
	private List<EFPoint> apex = new ArrayList<>();
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public List<EFPoint> getApex() {
		return apex;
	}
	public void setApex(List<EFPoint> apex) {
		this.apex = apex;
	}
	@Override
	public String toString() {
		return "ZoneGeometry [type=" + type + ", apex=" + apex + "]";
	}

}
