package smart.graze.core;



public class EFInfo {
	private Integer zone_id;
	private Integer style_id;
	private Integer data_id;
	private String title;
	
	private ZoneGeometry zone_geometry;

	public Integer getZone_id() {
		return zone_id;
	}

	public void setZone_id(Integer zone_id) {
		this.zone_id = zone_id;
	}

	public Integer getStyle_id() {
		return style_id;
	}

	public void setStyle_id(Integer style_id) {
		this.style_id = style_id;
	}

	public Integer getData_id() {
		return data_id;
	}

	public void setData_id(Integer data_id) {
		this.data_id = data_id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public ZoneGeometry getZone_geometry() {
		return zone_geometry;
	}

	public void setZone_geometry(ZoneGeometry zone_geometry) {
		this.zone_geometry = zone_geometry;
	}

	@Override
	public String toString() {
		return "EFInfo [zone_id=" + zone_id + ", style_id=" + style_id + ", data_id=" + data_id + ", title=" + title
				+ ", zone_geometry=" + zone_geometry + "]";
	}

}
